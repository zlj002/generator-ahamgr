<template >
  <el-form :model="ruleForm2"
    :rules="rules2"
    ref="ruleForm2"
    label-position="left"
    label-width="0px"
    class="demo-ruleForm login-container">
    <h3 class="title">
      <img src="../../../../assets/img/logo.png">
    </h3>
    <el-form-item prop="account">
      <el-input type="text"
        v-model="ruleForm2.account"
        auto-complete="off"
        placeholder="账号"></el-input>
    </el-form-item>
    <el-form-item prop="checkPass">
      <el-input type="password"
        v-model="ruleForm2.checkPass"
        auto-complete="off" 
        placeholder="密码"></el-input>
    </el-form-item>
    <!-- <el-checkbox v-model="checked"
      checked
      class="remember">记住密码</el-checkbox> -->
    <el-form-item style="width:100%;">
      <el-button type="primary"
        style="width:100%;"
        @click.native.prevent="login"
        :loading="logining">登录</el-button>
    </el-form-item>
  </el-form>
</template>

<script>
import { requestLogin } from '../../../api/api'
import { rawRoutes } from '../../routes'
var MenuUtils = require('menuUtils').default
//import NProgress from 'nprogress'
export default {
  data() {
    return {
      logining: false,
      ruleForm2: {
        account: '',
        checkPass: ''
      },
      rules2: {
        account: [
          { required: true, message: '请输入账号', trigger: 'blur' },
          //{ validator: validaePass }
        ],
        checkPass: [
          { required: true, message: '请输入密码', trigger: 'blur' },
          //{ validator: validaePass2 }
        ]
      },
      checked: true
    };
  },
  methods: {
    handleReset2() {
      this.$refs.ruleForm2.resetFields();
    },
    login(ev) {
      var _this = this;
      this.$refs.ruleForm2.validate((valid) => {
        if (valid) {
          //_this.$router.replace('/table');
          this.logining = true;
          // NProgress.start();
          var loginParams = { username: this.ruleForm2.account, password: this.ruleForm2.checkPass };
          requestLogin(loginParams).then(res => {
            this.logining = false;
            // NProgress.done();
            let { message, code, data } = res;
            if (code !== "0000") {
              this.$message({
                message: message,
                type: 'error'
              });
            } else {
              var routers = []
              sessionStorage.setItem('userPermission', JSON.stringify(data));
              MenuUtils('index', routers, data)
              this.$router.options.routes = rawRoutes
              this.$router.options.routes = this.$router.options.routes.concat(routers)
              this.$router.addRoutes(routers)
              // this.$router.push({ path: '/' })
              this.$router.push({ path: '/customer/complaint' })
            }
          });
        } else {
          console.log('error submit!!');
          return false;
        }
      });
    }
  },
  beforeCreate() {
    window.sessionStorage.removeItem('userPermission')
  },
  created() {
    var that = this;
    document.onkeydown = function (e) {
      var key = window.event.keyCode;
      if (key == 13) {
        that.login();
      }
    }
  }
}

</script>

